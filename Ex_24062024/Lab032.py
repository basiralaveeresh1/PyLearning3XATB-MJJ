# Add list items using the append() method
thislist = ["apple", "banana", "cherry"]
#To add an item to the end of the list, use the append() method:

thislist.append("orange")
print(thislist)

# Insert method
thislist.insert(1,"kiwi")
print(thislist)
# adding the item using index
thislist[1]="mango"
print(thislist)

#Extended method

thislist.extend(["pappaya","grapes"])
print(thislist)
